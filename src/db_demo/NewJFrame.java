    private void viewPateintsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewPateintsButtonActionPerformed
        // TODO add your handling code here:
        MenuPanel.setVisible(false);
        ViewPatientsPanel.setVisible(true);
        String sql = "Select * FROM patients p left join disease d ON (d.disease_id = p.disease_id)";
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hms?zeroDateTimeBehavior=CONVERT_TO_NULL","root","");
            pst = con.createStatement();
            ResultSet rs = pst.executeQuery(sql);
            while(rs.next())
            {
                String id = String.valueOf(rs.getInt("p.P_id"));
                String name = rs.getString("p.P_name");
                String age = String.valueOf(rs.getInt("p.Age"));
                String address = rs.getString("p.Address");
                String gender = rs.getString("p.Gender");
                String disease = rs.getString("d.Disease_name");
                String ward = String.valueOf(rs.getInt("p.Ward"));
                String data[] = {id, name, age, gender, disease, address, ward};
                tm = (DefaultTableModel)tablePatient.getModel();
                tm.addRow(data);
            }
            con.close();
        }
        catch (SQLException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_viewPateintsButtonActionPerformed

